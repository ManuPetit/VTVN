    <p class="t2">Les jardins étaient dans la rue<br />
      <br />
    </p>
    <p>Mardi 1er mai, fête du muguet, le traditionnel marché aux fleurs, organisé par l'association Vie et Travail dans le Vieux Nyons, a accueilli une vingtaine d'horticulteurs venus de toute la région pour présenter leurs productions. De véritables jardins de parterres fleuris ont jonché les rues de la vieille ville.
</p>
	<img src="../images/archives/a300_10.jpg" alt="Des jardins dans la rue" width="360" height="261" title="Des jardins dans la rue" />
	<p>Les connaisseurs furent nombreux de bon matin à détailler et à choisir... Dans les cultures exposées figuraient les habituels plants de légumes d'été, courgettes, aubergines, différentes tomates (olympe, roma, russe, st-pierre)... les plants d'herbes aromatiques, avec les basilic, persil, ciboulette, romarin, thym, menthe, ainsi que du framboisier, muflier, rhubarbe... les petits cactées, les arbustes, les petits et grands rosiers naturels et hybrides, les minuscules pieds de vignes et les lavandes papillon. Un petit olivier affichant ses quinze ans avoisinait un amoncellement de plantes tropicales, aux noms d'allaman-da, mandevilla, hibiscus, plumbago, bougainvillier, datura, palmiers... et une collection d'orchidées pour les amateurs.
</p>
<img src="../images/archives/a300_11.jpg" alt="Le marché aux fleurs" width="300" height="340" title="Le marché aux fleurs" />
<p>Pour les inconditionnels frémissaient les petites vivaces et annuelles, gaza-nia, pétunia, iris, les ficoïdes... Toujours présents, les «suspendus» «fleuris, les géraniums, les bouquets et les fleurs séchées, les pots décorés.. Et ici et là, les brins de muguet, à la tige- ou en pot... Sous un timide soleil, des nuages vadrouilleurs et quelques grosses gouttes transformées en averse, le décor végétal de la rue dans cette belle fraîcheur s'est empli d'aise : la verdure éclatante s'est étendue et les corolles aux multiples couleurs se sont inclinées vers des passants enchantés. Une belle journée, avec l'envie de garder la main verte...</p>
