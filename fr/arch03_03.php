    <p> Accord du Conseil R&eacute;gional sur le contrat de ville de Nyons.<br />
        Jean Monpeyssen et toute son &eacute;quipe sont satisfaits. Le dossier ficel&eacute; il y a quatre mois vient d'&ecirc;tre accept&eacute; par la r&eacute;gion pour un montant de 13 millions de francs. Les travaux s'&eacute;chelonneront sur trois ans.<br  /><br  />
      Cent communes des huit  d&eacute;partements de la r&eacute;gion Rh&ocirc;ne-Alpes    avaient    pos&eacute; des demandes pour obtenir un contrat de ville. 20 ont    &eacute;t&eacute;    retenues    dont dans   la   Dr&ocirc;me   Nyons, Donz&egrave;re     et     Chabeuil. D'autres	communes  avaient depuis longtemps pos&eacute; des dossiers. Nyons l'a d&eacute;pos&eacute;  pour la  premi&egrave;re fois cette ann&eacute;e et il a &eacute;t&eacute; accept&eacute;. On mesure donc la joie de Jean Monpeyssen :         &laquo; Nous avions d&eacute;j&agrave; fait un travail d'inventaire des besoins de   Nyons   et  nous   les avons inclus dans le contrat de ville.  C'est la Direction    D&eacute;partementale de   l'Equipement   qui  a travaill&eacute; avec nous sur le dossier &raquo;. Un bon travail d&rsquo;&eacute;quipe qui a pay&eacute; puisque 13 millions de francs sont d&eacute;bloqu&eacute;s sur les 18 millions initialement demand&eacute;s. Le Conseil R&eacute;gional ayant refus&eacute; de subventionner la construction d'un centre ludique pour 5 millions de francs.</p>

