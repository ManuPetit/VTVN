        D'abord la pr&eacute;paration &agrave; la r&eacute;alisation des grands projets structurants avec l'am&eacute;nagement de stationnement sur les berges de l'Eygues avec 400 places; La construction d'une passerelle sur l'Eygues et l'am&eacute;nagement de la zonetennis avec un concours d'architectes et d'urbanistes. Co&ucirc;t total de 3,3 millions de francs.<br  /><br  />
