    <p>L'association «Vie et travail dans le vieux Nyons » vient de créer son site.<br  />
Créée en 1984, l'association «Vie et travail dans le vieux Nyons» regroupait les habitants et commerçants de la vieille ville. Améliorer les conditions de vie et de travail des habitants et commerçants du vieux Nyons et promouvoir touristiquement et commercialement le quartier, motivaient chacun.<br  /><br  />
Les objectifs au cours des années suivantes furent une volonté commune des commerçants et des riverains de la rue des Déportés contre une désertification chronique et inacceptable après avoir été la rue principale pendant de nombreuses années. Une désertification amorcée à l'ouverture de la déviation et rendre à ce quartier une âme fut une priorité.
<br  /><br  />
Ainsi, différentes animations ont été mises en place et d'autres suscitées, comme le marché provençal. La participation aux diverses manifestations locales venait compléter les actions menées.
</p>
	<img src="../images/archives/a200_03.jpg" alt="Les membres de l'association VTVN." width="300" height="129" title="Les membres de l'association VTVN." />
	
	<p>En 2006, les commerçants adhérents de l'association, anciens et nouveaux, restent solidaires. Et pour mieux communiquer, l'un d'entre eux, Emmanuel Petit - du restaurant Le Tiroir à Chats - qui «se débrouille» en informatique, a spontanément et gracieusement proposé la création de leur site Internet. Ce qui fut fait en trois semaines. Aujourd'hui, le« Vieux Nyons» est passé à la vitesse supérieure !
<br  /><br  />
En quelques clics, vous pouvez surfer dans le monde si particulier et très convivial de la rue des Déportés jusqu'au Pont Roman ainsi que la place du Colonel Barillon. C'est une visite virtuelle pour connaître les 35 commerces, restaurants, alimentation, mode et beauté, art et décoration, services, découvrir les rubriques «événements», «archives», «histoire» «galerie photos», des pages très intéressantes, indissociables de notre culture.<br  />
Et à noter, la page « lien » qui relie à l'office de tourisme, la mairie...<br  /><br  />
Tous ont levé leur verre en remerciant Emmanuel !<br  />
Un seul contact à retenir : www.vieuxnyons.com
