    <p>Lundi 1er mai, le 16e marché aux fleurs, organisé par l'association «Vie et travail dans le vieux Nyons », s'est tenu tout le jour, pour le bonheur des amoureux de la nature, jardiniers en herbe, amateurs et professionnels. Feuillages d'arbustes, pétales, corolles et clochettes de fleurs s'étiraient de la place colonel Barillon jusqu'au pont roman sous un ciel à nouveau exceptionnel. De très bonne heure sous un beau soleil, une foule dense arpentait déjà l'allée à la recherche de «sa» plante, atteinte parfois d'un coup de cœur pour un frêle et suave végétal.<br /><br  />
17 exposants étaient présents et la nature avait envahi la rue en parterres colorés...
</p>
	<img src="../images/archives/a300_12.jpg" alt="Les visiteurs du 16ème marché aux fleurs" width="300" height="204" title="Les visiteurs du 16ème marché aux fleurs" />
	<p>Des nouveautés cette année, des pieds de vignes, des plants de melons, qui s'alignaient parmi les rangées de plants potagers et les plantes aromatiques, verveine, romarin, menthe, etc.<br /><br  />
Toujours présents, les pieds de lavandes, les arbrisseaux d'oliviers, les bougainvilliers, les jeunes cactées en pots... les fleurs vivaces et les annuelles, iris, œillets, bégonias, gueules-de-loup, arums, impatiences... et le muguet au parfum délicat.<br /><br  />
Les cache-pots décorés, travail d'artisanat, s'exposaient comme chaque année, de même que l'incontournable de la connaissance, des livres tels que « La Drôme insolite », « Carnet de voyage », des CD comme «Concert des Garrigues ».<br /><br  />
Les promeneurs, tout au long du jour, ont déambulé et sont repartis souriants, détendus et les bras chargés. Un plaisir partagé par les membres de l'association...
</p>
